export const dictionaries: Record<Locale, Record<I18nKey, string>> = {
  'zh-CN': zhCN,
  'en-US': enUS
};

export function translate(locale: Locale, key: I18nKey, params: Record<string, string | number> = {}): string {
  const template = dictionaries[locale][key] ?? dictionaries['zh-CN'][key] ?? key;
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function getMissingTranslationKeys() {
  const zhKeys = Object.keys(zhCN);
  return LOCALES.flatMap((locale) => zhKeys.filter((key) => !dictionaries[locale][key as I18nKey]).map((key) => `${locale}:${key}`));
}
